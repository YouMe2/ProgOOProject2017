/**
 * This enum serves as a enumeration of all possible inputactions.
 * 
 * 
 */
public enum InputActionKey {

	/**
	 * pressed UP
	 */
	UP_P,
	/**
	 * pressed DOWN
	 */
	DOWN_P,
	/**
	 * pressed LEFT
	 */
	LEFT_P,
	/**
	 * pressed RIGHT
	 */
	RIGHT_P,
	/**
	 * pressed SELECT
	 */
	SELECT_P,
	
	/**
	 * released UP
	 */
	UP_R, 
	/**
	 * released DOWN
	 */
	DOWN_R, 
	/**
	 * released LEFT
	 */
	LEFT_R,
	/**
	 * released RIGHT
	 */
	RIGHT_R,
	/**
	 * released SELECT
	 */
	SELECT_R

}
